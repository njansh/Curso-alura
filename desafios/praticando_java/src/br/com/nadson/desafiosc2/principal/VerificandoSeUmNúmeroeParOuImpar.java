public class VerificandoSeUmNúmeroeParOuImpar {
    public static void main(String[] args) {
        int numero=7;
        if(numero % 2 == 0){
            System.out.println(numero+" é um numero par");
        }else{
            System.out.println(numero+" é um numero impar");

        }
         numero=8;
        if(numero % 2 == 0){
            System.out.println(numero+" é um numero par");
        }else{
            System.out.println(numero+" é um numero impar");

        }
    }
}
